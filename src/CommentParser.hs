-- Файл: src/CommentParser.hs
-- Описание:
-- Модуль для парсинга комментариев в языке программирования.
-- Используется библиотека Megaparsec для обработки строк и комментариев.
-- В данной версии реализована только функция для пропуска комментариев вида (* ... *).

module CommentParser where

import Text.Megaparsec
import Text.Megaparsec.Char
import Data.Void

-- | Определение типа парсера
-- Parsec Void String — это парсер, который обрабатывает строки и не использует состояния (Void).
type Parser = Parsec Void String

-- | Функция для пропуска комментариев вида (* ... *).
-- Она начинает с поиска строки "(*", затем пропускает все символы до строки "*)".
skipComment :: Parser ()
skipComment = do
  _ <- string "(*"            -- Ищем начало комментария "(*"
  _ <- manyTill anySingle (string "*)")  -- Пропускаем все символы до конца комментария "*)"
  return ()  -- Возвращаем пустой результат, так как нам нужно только пропустить комментарий
